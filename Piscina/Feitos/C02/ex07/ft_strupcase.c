char	*ft_strupcase(char *str)
{
	int		i;
	char	change;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 97 && str[i] <= 122)
		{
			change = str[i] - 32;
			str[i] = change;
		}

		i++;
	}
	str[i] = '\0';
	return (str);
}
